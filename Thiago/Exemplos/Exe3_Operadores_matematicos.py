# Inicio
# Variáveis
# Substituir para entrada do usuário (print)
numeroUm = 10
numeroDois = 5
resultado = 0 # Variável para receber o resultado da operação
# Soma
resultado = numeroUm + numeroDois
print(numeroUm, ' + ', numeroDois, ' = ', resultado)
# Subtração
resultado = numeroUm - numeroDois
print(numeroUm , ' - ', numeroDois, ' = ', resultado)
# Multiplicação
resultado = numeroUm * numeroDois 
print(numeroUm , ' X ', numeroDois, ' = ', resultado)
# Divisão
resultado = numeroUm / numeroDois
print(numeroUm, ' / ', numeroDois, ' = ', resultado)
