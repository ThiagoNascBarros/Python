# Inicio
# String -> palavras 'utiliza-se aspas simples ou duplas'
computador = 'DELL'
print('A variável computador é do tipo: ', type(computador))
# Integer(inteiro) -> Números inteiros exeplos: 1; 2; 3; 4; 5; ...;
idade = 53
print('A variável idade é do tipo: ', type(idade))
# Float(flutuante) -> Números fracionados exeplo: 1.3 e 2.5
altura = 1.9
print('A variável altura é do tipo: ', type(altura))
# Bool (Boolean) -> True(Verdadeiro) ou False(Falso) 
palmeiras_tem_mundial = False
print('A variável palmeiras tem mundial é do tipo: ', palmeiras_tem_mundial, type(palmeiras_tem_mundial)) 
